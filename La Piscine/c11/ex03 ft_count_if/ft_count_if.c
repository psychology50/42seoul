int	ft_count_if(char **tab, int length, int(*f)(char *))
{
	int	idx;
	int	cnt;

	idx = 0;
	cnt = 0;
	while (idx < length)
	{
		if (f(tab[idx]) != 0)
			cnt++;
		idx++;
	}
	return (cnt);
}